    growth_time INTEGER NOT NULL,
    soil_dryness INTEGER NOT NULL,
    smoothness INTEGER NOT NULL,
    FOREIGN KEY (firmness_id) REFERENCES berry_firmness(id),
    FOREIGN KEY (natural_gift_type_id) REFERENCES types(id)
